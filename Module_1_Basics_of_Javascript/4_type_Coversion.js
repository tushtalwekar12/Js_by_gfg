/* Type Conversion -
to string
to Number
to boolean
*/

const a = 10;
const b = 12;

// convert to string 
const c = String(a);   
console.log(typeof a);   // number
console.log(typeof c);   // string

// convert to Boolean
const d = Boolean(b);
const e = 0;
const f = Boolean(e);
console.log(d);  // true
console.log(f);  // false

const z ="prakash";
const y = Number(z);
console.log(y);  // NaN (Not a Number)

const x ="tushar";
const w = Boolean(x);
console.log(w);  // true





